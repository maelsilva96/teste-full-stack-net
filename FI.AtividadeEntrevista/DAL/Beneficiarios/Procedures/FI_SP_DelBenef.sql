CREATE PROC FI_SP_DelBenef
	@ID_CLIENTE BIGINT,
	@CPFS_QUE_NAO_SERAO_EXCLUIDOS NVARCHAR(MAX) /* Ex.: 458795854722, 458795854723, 458795854742 */
AS
BEGIN
	DELETE
		BENEFICIARIOS 
	WHERE 
		IDCLIENTE = @ID_CLIENTE AND 
		CPF NOT IN(SELECT value FROM STRING_SPLIT(@CPFS_QUE_NAO_SERAO_EXCLUIDOS, ','))
END